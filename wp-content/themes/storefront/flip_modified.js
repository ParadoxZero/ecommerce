$("#" + "sec-1-card-1").mouseenter(function(e) {
    $(function() {
        var timeoutId;
        var hoverId = "sec-1-card-1";
        $('body').append('#' + hoverId);
        $('#' + hoverId).hover(function() {
                if (!timeoutId) {
                    timeoutId = window.setTimeout(function() {
                        timeoutId = null;
                        $('#' + hoverId).flip(true);
                    }, 800);
                }
            },
            function () {
                if (timeoutId) {
                    window.clearTimeout(timeoutId);
                    timeoutId = null;
                }
                else {
                    $('#' + hoverId).flip(false);
                }
            });

    });
});

